#[test]
fn fill_down_basic() {
    let input = "a\tb\tc
1\tx\t
\ty\t20
2\t\t30
\tz\t
";
    // Col 1: 1, "", 2, "" -> 1, 1, 2, 2
    // Col 2: x, y, "", z -> x, y, y, z
    // Col 3: "", 20, 30, "" -> "", 20, 30, 30 (first empty stays empty)
    let expected = "
a\tb\tc
1\tx\t
1\ty\t20
2\ty\t30
2\tz\t30
";
    let (result, _) = TvaCmd::new()
        .stdin(input)
        .args(&["fill", "--header", "--field", "1,2,3"])
        .run();

    assert_eq!(result.trim(), expected.trim());
}

